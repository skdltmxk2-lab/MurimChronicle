async function updateIds(ids, subject, unit) {
  if (!apply || ids.length === 0) return;
  const { error } = await supabase
    .from("questions")
    .update({ subject, unit, updated_at: new Date().toISOString() })
    .in("id", ids);
  if (error) throw error;
}

console.log(`[normalize-units-round3] mode=${apply ? "APPLY" : "DRY-RUN"}`);
let total = 0;

for (const [fromSubject, fromUnit, toSubject, toUnit] of directRules) {
  const { data, error } = await supabase
    .from("questions")
    .select("id, concept")
    .eq("subject", fromSubject)
    .eq("unit", fromUnit);
  if (error) throw error;

  const rows = data ?? [];
  if (rows.length === 0) continue;
  const ids = rows.map((row) => row.id);
  total += ids.length;
  console.log(
    `${String(ids.length).padStart(4)}  ${fromSubject}|${fromUnit} -> ${toSubject}|${toUnit}  ex=${rows
      .slice(0, 3)
      .map((row) => `${row.id}:${row.concept ?? ""}`)
      .join(", ")}`
  );
  await updateIds(ids, toSubject, toUnit);
}

console.log(`[normalize-units-round3] target rows=${total}`);
