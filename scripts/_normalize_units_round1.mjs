let total = 0;
console.log(`[normalize-units-round1] mode=${apply ? "APPLY" : "DRY-RUN"}`);

for (const [fromSubject, fromUnit, toSubject, toUnit] of rules) {
  const { data, error } = await supabase
    .from("questions")
    .select("id")
    .eq("subject", fromSubject)
    .eq("unit", fromUnit);
  if (error) throw error;
  const ids = (data ?? []).map((row) => row.id);
  if (ids.length === 0) continue;
  total += ids.length;
  console.log(
    `${String(ids.length).padStart(4)}  ${fromSubject}|${fromUnit} -> ${toSubject}|${toUnit}  ex=${ids.slice(0, 5).join(",")}`
  );
  if (!apply) continue;
  const { error: updateError } = await supabase
    .from("questions")
    .update({
      subject: toSubject,
      unit: toUnit,
      updated_at: new Date().toISOString(),
    })
    .in("id", ids);
  if (updateError) throw updateError;
}

console.log(`[normalize-units-round1] target rows=${total}`);
